import UIKit

extension UIFont {
    
    static func binggrae(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Binggrae", size: size)!
    }

    static func binggraeBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Binggrae-Bold", size: size)!
    }
}
